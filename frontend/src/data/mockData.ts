// Helper functions
export const getDoctorById = (id: number): Doctor | undefined => {
  return mockDoctors.find(doctor => doctor.id === id);
};

export const getClinicById = (id: number): Clinic | undefined => {
  return mockClinics.find(clinic => clinic.id === id);
};

export const getDoctorsByClinicId = (clinicId: number): Doctor[] => {
  return mockDoctors.filter(doctor => doctor.clinicId === clinicId);
};

export const getClinicByDoctorId = (doctorId: number): Clinic | undefined => {
  const doctor = getDoctorById(doctorId);
  return doctor ? getClinicById(doctor.clinicId) : undefined;
};

